public class VariableDemo68 {
  public static void main(String[] args) {
    int x = 12;
    int y = 3;
    System.out.println(x / y);
    System.out.println("-------------");
    System.out.println(x * y);
    System.out.println("-------------");
    System.out.println(x + y);
    System.out.println("-------------");
    System.out.println(x - y);
    System.out.println("-------------");
  }
}