public class VariableDemo73 {
  static void myMethod(String fname, int age) {
    System.out.println(fname + " is " + age);
  }

  public static void main(String[] args) {
    myMethod("Lion", 5);
    myMethod("Jenny", 8);
    myMethod("shizika", 26);
    myMethod("Mou", 31);
    myMethod("david", 40);
  }
}
