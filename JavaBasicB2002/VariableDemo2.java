//VariableDemo2.java
public class VariableDemo2{
    public static void main(String[] args){
        System.out.println("I've got nice dinner tonight!");
        int myNum=5;
        float myFloatNum=5.9912345f;
        System.out.print(myNum + myFloatNum);

        char MyLetter ='D', a=65,b=66,c=67;
        System.out.println(MyLetter + a + b + c);
        System.out.println(MyLetter);

        boolean myBool= true;
        String myText="Hello!";
        byte myNum2=100;
        System.out.println(myNum2 + myText + myBool);

        short myNum3 =5000;
        System.out.println(myNum3);

        long myNum4=1500000000L;
        System.out.println(myNum4);
        
        double myNum5=19.987654321012345d;
        System.out.println(myNum5);

        
    }
}