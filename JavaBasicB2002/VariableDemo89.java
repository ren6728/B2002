public class VariableDemo89 {
  public static void main(String[] args) {
    int day = 3;
    switch (day) {
      case 4:
        System.out.println(" hey ,Thursday");
        break;
      case 5:
        System.out.println(" hi, Friday");
        break;
      case 6:
        System.out.println("Today is Saturday");
        break;
      case 7:
        System.out.println("Today is Sunday");
        break;
      default:
        System.out.println("Looking forward to the Weekend");
    }    
  }
}