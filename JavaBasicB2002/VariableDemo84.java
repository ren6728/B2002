public class VariableDemo84 {
  public static void main(String[] args) {
    String greeting = "Hello";
    System.out.println(greeting);
    System.out.println("--------------------");
    System.out.println(greeting.toUpperCase());
    System.out.println(greeting.toLowerCase());
    System.out.println("--------------------");
    String txt1 = "It's alright";
    String txt2 = "That's great";
    System.out.println(txt1);
    System.out.println(txt2);
    System.out.println("--------------------");
    System.out.println(txt1.indexOf("l"));
    System.out.println(txt2.indexOf("t"));
    System.out.println("--------------------");
    System.out.println("The length of the txt string is: " + txt1.length());
    System.out.println("The length of the txt string is: " + txt2.length());
    System.out.println("--------------------");
    String firstName = "He";
    String lastName = "xiang";
    System.out.println(firstName + " " + lastName);
    System.out.println("--------------------");
    String x = "1000000";
    int y = 20;
    String z = x + y;
    System.out.println(z);
    System.out.println("--------------------");
  }
}
