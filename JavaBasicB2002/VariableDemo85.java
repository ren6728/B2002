public class VariableDemo85 {
  public static void main(String[] args) {
    System.out.println(Math.max(-5, 10));  
    System.out.println(Math.min(0, 0.1)); 
    System.out.println(Math.sqrt(65)); 
    System.out.println(Math.abs(-0.76));  
    System.out.println(Math.random());  
    System.out.println("--------------------");
    System.out.println(Math.max(-100, 100));  
    System.out.println(Math.min(1/2, 0.1)); 
    System.out.println(Math.sqrt(7-5)); 
    System.out.println(Math.abs(0.76));  
    System.out.println(Math.random());  
  }
}